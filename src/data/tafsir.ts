/**
 * Get tafsir entry for a specific surah and ayah.
 * Returns null if no tafsir is available for the given reference.
 */
export function getTafsir(surah: number, ayah: number): TafsirEntry | null {
  const key = `${surah}:${ayah}`;
  return TAFSIR_DATA[key] ?? null;
}

/**
 * Check if tafsir is available for a specific surah and ayah.
 */
export function hasTafsirForAyah(surah: number, ayah: number): boolean {
  const key = `${surah}:${ayah}`;
  return key in TAFSIR_DATA;
}

/**
 * Check if tafsir exists for any ayah in a given surah.
 */
export function hasTafsir(surah: number): boolean {
  return Object.keys(TAFSIR_DATA).some((key) => key.startsWith(`${surah}:`));
}

/**
 * Get all tafsir entries for a given surah, sorted by ayah number.
 */
export function getSurahTafsir(surah: number): TafsirEntry[] {
  return Object.entries(TAFSIR_DATA)
    .filter(([key]) => key.startsWith(`${surah}:`))
    .map(([, entry]) => entry)
    .sort((a, b) => a.ayah - b.ayah);
}

/**
 * Get all tafsir entries for a given surah.
 * Returns entries sorted by ayah number.
 */
export function getTafsirBySurah(surah: number): TafsirEntry[] {
  return getSurahTafsir(surah);
}

/**
 * Get all surah numbers that have at least one tafsir entry.
 */
export function getSurahsWithTafsir(): number[] {
  const surahs = new Set<number>();
  for (const entry of Object.values(TAFSIR_DATA)) {
    surahs.add(entry.surah);
  }
  return Array.from(surahs).sort((a, b) => a - b);
}

/**
 * Get the total count of tafsir entries.
 */
export function getTafsirCount(): number {
  return Object.keys(TAFSIR_DATA).length;
}
